			      <a id="background">&nbsp</a> <br><br><h1><font color="#cd4259">Background of the Innovation</font></h1>

		<h4><p align="justify">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe communication gap between the administration and people was conceived to be a major factor to address for effective and efficient governance.

			The citizens lack due knowledge about which office to approach and which documents and information to provide when they approach the government administration for services. There is no clarity to the citizens about the time limit and process-flow related to their applications. Citizens have to submit the same information repeatedly to the same or related department. Citizens spend a lot of time and money for aggressive follow-ups with the department with the hope to expedite the work.

			The Administrative staff often receives incomplete applications.</p><p align="justify">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspThis creates a need to communicate with the applicants about the deficiencies. The administration also has to spend a lot of energy in handling such follow-ups instead of focusing on the actual work. As a result, there are delays, lapses, costs and inconveniences to the citizens and loss of efficiency and undue work pressures for the administrative staff. The senior officers also lack accurate and updated information on the status of work-in-process and have to count on information provided by the junior staff.

			The gaps between administration and citizens at times is unhealthily filled-up by the agents and touts leading to inadequate practices and loss of people’s confidence in the Administration.

			</p><p align="justify">&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbspInformation and Communication Technology (ICT) has contributed significantly to the efficiency and effectiveness of the industries and organizations. ICT has also empowered the customer and service recipient by ensuring better services and good control over the process of receipt of services.              
